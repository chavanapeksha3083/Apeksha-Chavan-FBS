

#include<stdio.h>

void main()
{

 printf("Even Numbers = ");
for(int k=1;k<=15;k++)
{
	
int n=k;


  	if(n%2==0)
  	{
  		printf(" %d ",k);
	  }
	  
  
}

 printf("\nOdd Numbers = ");
for(int k=10;k<=25;k++)

  {
  	int n=k;

  	if(n%2!=0)
  	{
  		
  		printf(" %d" ,k);
	  }
  }
  
 
}

