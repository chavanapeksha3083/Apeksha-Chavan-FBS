/* 3. Write a C program to convert temperature from Celsius to Fahrenheit using the
formula: 
F = (C *9/5) + 32
*/

#include<stdio.h>
void main()
{
	 int C=25;
	 float F=(C*9.0/5)+32;

	 printf("Temperature is:%d",C);
	 printf("\nTemperature from Celsius to Fahrenheit is:%f",F);

}

	